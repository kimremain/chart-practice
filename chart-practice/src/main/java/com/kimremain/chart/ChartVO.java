package com.kimremain.chart;

public class ChartVO {
	
	private String year;	
	private int hp;
	private int dell;
	private int ibm;
	
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public int getHp() {
		return hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
	public int getDell() {
		return dell;
	}
	public void setDell(int dell) {
		this.dell = dell;
	}
	public int getIbm() {
		return ibm;
	}
	public void setIbm(int ibm) {
		this.ibm = ibm;
	}

	
}
